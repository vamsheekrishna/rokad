package com.rokad.dmt.RequestPackets;

public class CommissionDetails {

    private String commissionGained;
    private String commissionDistributed;


    public String getCommissionGained() {
        return commissionGained;
    }

    public void setCommissionGained(String commissionGained) {
        this.commissionGained = commissionGained;
    }

    public String getCommissionDistributed() {
        return commissionDistributed;
    }

    public void setCommissionDistributed(String commissionDistributed) {
        this.commissionDistributed = commissionDistributed;
    }
}
